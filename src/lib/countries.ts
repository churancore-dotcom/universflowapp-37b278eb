export const flagFor = (cc?: string | null): string => {
  if (!cc) return '🌍';
  return COUNTRIES.find((c) => c.code === cc.toUpperCase())?.flag || '🌍';
};

export const nameFor = (cc?: string | null): string => {
  if (!cc) return '';
  return COUNTRIES.find((c) => c.code === cc.toUpperCase())?.name || cc;
};
